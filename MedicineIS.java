    //Method for counting Category index
    public int countCategory(){
        String category = CategoryBox.getSelectedItem().toString();
        int countCat = 0;
        
        for(MedicineTableData detail: Mdata){
            
            if (category.equals(detail.getCategoryBox())){
                CateNames.add(detail.getNameTxt());
                countCat++;
            }
        }
        return countCat;
    }
    
    
    //ADD button
    private void ButAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButAddActionPerformed
        String SerialNo = SerialNumTxt.getText().trim();
        String Name = NameTxt.getText().trim();
        String Type = TypeTxt.getText().trim();
        String Category = CategoryBox.getSelectedItem().toString();
        String Price = PriceTxt.getText().trim();
        String Mfg = MfgTxt.getText().trim();
        String Exp = ExpTxt.getText().trim();
        int SerialNumber, Mdprice;
        
        DefaultTableModel dfm = (DefaultTableModel)DatabaseTable.getModel();
        String [] st = {SerialNo, Name,Type, Category, Price, Mfg, Exp};
        if(SerialNo.equals("") || Name.equals("") || Type.equals("") || CategoryBox.getSelectedIndex() == 0 || Price.equals("") || Mfg.equals("") || Exp.equals("")){
            JOptionPane.showMessageDialog(this,"Please Enter data","Empty Input",2);
        }
        else
        {
            try{
                SerialNumber = Integer.parseInt(SerialNo);
                Mdprice = Integer.parseInt(Price);
                int rowCount = DatabaseTable.getRowCount();
                for(int i = 0; i<rowCount; i++){
                    String SerialNum = (String)DatabaseTable.getValueAt(i,0);
                    if(SerialNo.equals(SerialNum))
                    {
                        JOptionPane.showMessageDialog(this,"Data for this medicine already exits.","Repeated data",1);
                        return;
                    }
                }
                if(!Pattern.compile("[0-9]{4}").matcher(SerialNo).matches())
                {
                    JOptionPane.showMessageDialog(this,"Invalid serial number","Invalid input",0);
                    return;
                }
                dfm.addRow(st);
                MedicineTableData data = new MedicineTableData(SerialNumber, Name, Type, Category, Mdprice, Mfg, Exp);
                Mdata.add(data);
                loadCSV();
            } 
            catch(NumberFormatException nfe){
                JOptionPane.showMessageDialog(this,"Enter the details in correct format","Invalid Input",0);
            }
            
        }
    }//GEN-LAST:event_ButAddActionPerformed
    
