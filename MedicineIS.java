    //Selection sorting
    public void selectionSort(){
        for(int i = 0; i < Mdata.size()-1; i++)
        {
            int minPos = i;
            for(int j = i +1; j < Mdata.size();j++)
            {
                if(Mdata.get(minPos).getSerialNumTxt() > Mdata.get(j).getSerialNumTxt())
                {
                    minPos = j;
                }
            }
            Collections.swap(Mdata,minPos,i);
        }
    }
    
    
    //Binary searching
    
    public static int binarySearch(ArrayList<MedicineTableData> Mdata, int start, int stop, int search){
        if (start <= stop){
            int middleValue = (start + stop)/2;
            
            if (Mdata.get(middleValue).getPriceTxt() == search){
                    return middleValue;
            }
            else if (Mdata.get(middleValue).getPriceTxt() > search){
                return binarySearch(Mdata, start, middleValue-1, search);
            }
            else{
                return binarySearch(Mdata, middleValue+1, stop, search);
            }
        }
        else{
            return -1;
        }
        
    }
    
