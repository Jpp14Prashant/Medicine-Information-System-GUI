    //Creating CSV file for data storing 
    public void loadCSV() 
    {
        try {
            File f1 = new File("MedicineData.csv");
            if (!f1.isFile()) 
            {
                f1.createNewFile();
            }
            FileWriter fw = new FileWriter(f1);
            for(MedicineTableData mtd:Mdata)
            {
                String values[] = {mtd.getSN(),mtd.getNameTxt(),mtd.getTypeTxt(),mtd.getCategoryBox(),mtd.getPrice(),mtd.getMfgTxt(),mtd.getExpTxt()};
                for( int i = 0; i < values.length;i++)
                {
                    fw.append(values[i]);
                    if(i == values.length-1)
                    {
                        break;
                    }
                    fw.append(",");
                }   
                fw.append("\n");
            }
            fw.flush();
            fw.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,"IO Exception occured");
        }
    }
    
    //Reading the CSV file 
    public void readCSV() {
        try {
            BufferedReader bffr = new BufferedReader(new FileReader("MedicineData.CSV"));
            String row = "";
            while ((row = bffr.readLine()) != null) {
                String Values[] = row.split(",");
                DefaultTableModel dtm = (DefaultTableModel) DatabaseTable.getModel();
                dtm.addRow(Values);
                MedicineTableData data0 = new MedicineTableData(Integer.parseInt(Values[0]), Values[1], Values[2], Values[3],Integer.parseInt(Values[4]),  Values[5], Values[6]);
                Mdata.add(data0);
            }
        } catch (FileNotFoundException fne) {
            JOptionPane.showMessageDialog(this, "File not Found");
        } catch (IOException ioe) {
            JOptionPane.showMessageDialog(this, "IOExeption occured");
        } catch (IndexOutOfBoundsException iobe){
            JOptionPane.showMessageDialog(this, "IndexOutOfBoundsException occured");
        }
    }
    
