    //Search price button
    private void ButSearchPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButSearchPriceActionPerformed
        String priceAmt = SearchPriceTxt.getText().trim();
        
        if(priceAmt.isEmpty()){
            JOptionPane.showMessageDialog(this,"Please enter the price of the medicine you want to search!","Search",2);
        }
        else{
            try{
                int searchedPrice = Integer.parseInt(priceAmt);
                selectionSort();
                int searchResult = binarySearch(Mdata, 0, Mdata.size()-1, searchedPrice);
                if (searchResult == -1){
                    JOptionPane.showMessageDialog(this, "Sorry, medicine of price Rs." + searchedPrice + " was not found.", "Message", 1);
                    return;
                }
                else{
                    JOptionPane.showMessageDialog(this, "SerialNo= " + Mdata.get(searchResult).getSerialNumTxt() + "\nName= " +
                    Mdata.get(searchResult).getNameTxt() + "\nType= " + Mdata.get(searchResult).getTypeTxt() + "\nCategory= " + Mdata.get(searchResult).getCategoryBox()
                    + "\nPrice=" + Mdata.get(searchResult).getPriceTxt() + "\nMfg Date=" + Mdata.get(searchResult).getMfgTxt() + "\nExpiry date=" + Mdata.get(searchResult).getExpTxt(), "Message", 1);
                    SearchPriceTxt.setText("");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this,"Exception occured","Error",0);
            }
        }
    }//GEN-LAST:event_ButSearchPriceActionPerformed

